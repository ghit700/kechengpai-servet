package com.kechengpai.bean;

public class Course {
	private int c_id;
	private String name;
	private String teacher;
	private String code;
	private int numbers;
	public Course(int c_id, String name, String teacher, String code,
			int numbers) {
		super();
		this.c_id = c_id;
		this.name = name;
		this.teacher = teacher;
		this.code = code;
		this.numbers = numbers;
	}
	
	
}
